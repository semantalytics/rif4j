package at.sti2.rif4j;

/**
 * Defines the required constant values for RIF.
 * 
 * @author Adrian Marte
 */
public class Namespaces {

	/**
	 * The namespace of RIF.
	 */
	public static final String RIF_NAMESPACE = "http://www.w3.org/2007/rif#";

	/**
	 * The namespace of RIF function built-ins.
	 */
	public static final String RIF_FUNC = "http://www.w3.org/2007/rif-builtin-function#";

	/**
	 * The namespace of RIF predicate built-ins.
	 */
	public static final String RIF_PRED = "http://www.w3.org/2007/rif-builtin-predicate#";

	/**
	 * The XML namespace.
	 */
	public static final String XML_NAMESPACE = "http://www.w3.org/XML/1998/namespace";

	/**
	 * The XML schema namespace.
	 */
	public static final String XSD_NAMESPACE = "http://www.w3.org/2001/XMLSchema#";

	/**
	 * The RDF namespace.
	 */
	public static final String RDF_NAMESPACE = "http://www.w3.org/1999/02/22-rdf-syntax-ns#";

}
